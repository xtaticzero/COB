public class CreditoFiscal {

    private String idCptoLey;
    private String idMotivo;
    private String idImporte;

    public String getIdCptoLey() {
        return idCptoLey;
    }

    public void setIdCptoLey(String idCptoLey) {
        this.idCptoLey = idCptoLey;
    }

    public String getIdMotivo() {
        return idMotivo;
    }

    public void setIdMotivo(String idMotivo) {
        this.idMotivo = idMotivo;
    }

    public String getIdImporte() {
        return idImporte;
    }

    public void setIdImporte(String idImporte) {
        this.idImporte = idImporte;
    }
}
